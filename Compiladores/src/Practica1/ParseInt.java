package Practica1;

public class ParseInt {
	
	/*
	 * Integer.parseInt(String s) devuelve el valor numerico entero de la cadena de digitos s
	 */
	
	public static void main(String[] args) {
		String numero = "2020";
		System.out.println("El valor numerico de " + numero + " es " + Integer.parseInt(numero));
	}
}
